"""Shared exclusion logic for all coding-standards hooks.

A file is excluded from coding-standards enforcement if ANY of:

1. Its path matches a built-in default pattern (shadcn/ui, generated code,
   migrations, vendored deps, build outputs, lock files).
2. Its content has a generation marker in the first 10 lines (`@generated`,
   `DO NOT EDIT`, `Code generated by`, etc.).
3. Its path matches a pattern in the project's `.coding-standards-ignore`
   (gitignore-style, located at the project root).

Stdlib only. Used by every block-*.py hook and (via the SKILL.md directive)
by the orchestrator and workers.
"""

from __future__ import annotations

import re
from pathlib import Path
from typing import Iterable

# ─────────────────────────────────────────────────────────────────────────────
# Built-in default exclusions.
# Patterns are gitignore-style:
#   `*` matches any single path segment (no `/`)
#   `**` matches zero or more path segments
#   patterns without `/` match by filename anywhere
# ─────────────────────────────────────────────────────────────────────────────

