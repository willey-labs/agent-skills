def _segment_to_regex(segment: str) -> str:
    """Convert a single path segment (no `/`) glob to a regex body.

    Within a segment, `*` matches any chars except `/`, `?` matches one char
    except `/`, `[..]` is a character class.
    """
    out: list[str] = []
    i = 0
    while i < len(segment):
        ch = segment[i]
        if ch == "*":
            out.append(r"[^/]*")
            i += 1
        elif ch == "?":
            out.append(r"[^/]")
            i += 1
        elif ch == "[":
            end = segment.find("]", i + 1)
            if end == -1:
                out.append(re.escape(ch))
                i += 1
            else:
                out.append(segment[i : end + 1])
                i = end + 1
        else:
            out.append(re.escape(ch))
            i += 1
    return "".join(out)


def _pattern_to_regex(pattern: str) -> re.Pattern[str]:
    """Convert a gitignore-style glob pattern to a compiled regex.

    Semantics:
      `*`     within a segment, matches any chars except `/`
      `**/`   at start: matches zero or more leading directory segments (incl. trailing `/`)
      `/**`   at end: matches `/anything` or nothing (the directory's contents)
      `/**/`  in middle: matches `/anything/` between two literal segments
      `?`     matches one char except `/`
      `[..]`  character class

    Pattern without `/` matches by filename anywhere in the path.
    Pattern starting with `/` is anchored at path start.
    Pattern with `/` (but not leading) matches at any path depth.
    """
    if "/" not in pattern:
        body = _segment_to_regex(pattern)
        return re.compile(rf"(?:^|.*/)({body})$")

    out: list[str] = []
    i = 0
    n = len(pattern)
    while i < n:
        # Order matters — match multi-char sequences first.
        if pattern.startswith("**/", i):
            # `**/` — zero or more leading segments (or empty).
            # `.+/` matches at least one char + trailing `/`. `?` makes the
            # whole group optional, so `**/foo` matches `foo` at depth 0 too.
            out.append(r"(?:.+/)?")
            i += 3
            continue
        if pattern.startswith("/**", i):
            if i + 3 == n:
                # Trailing `/**` — match `/anything` or nothing.
                out.append(r"(?:/.*)?")
                i += 3
                continue
            if i + 3 < n and pattern[i + 3] == "/":
                # `/**/` — match `/` + zero or more segments + `/`.
                out.append(r"/(?:[^/]+/)*")
                i += 4
                continue
            # `/**foo` (uncommon) — fall through to single `/`
        if pattern[i] == "*":
            if i + 1 < n and pattern[i + 1] == "*":
                # Bare `**` not at a recognized boundary — treat as `.*`.
                out.append(r".*")
                i += 2
                continue
            out.append(r"[^/]*")
            i += 1
            continue
        if pattern[i] == "?":
            out.append(r"[^/]")
            i += 1
            continue
        if pattern[i] == "[":
            end = pattern.find("]", i + 1)
            if end == -1:
                out.append(re.escape(pattern[i]))
                i += 1
            else:
                out.append(pattern[i : end + 1])
                i = end + 1
            continue
        # Literal character (including `/`).
        out.append(re.escape(pattern[i]))
        i += 1

    body = "".join(out)
    if pattern.startswith("/"):
        return re.compile(rf"^{body}$")
    # Non-leading-slash patterns that contain a `/` match at ANY path depth
    # (per this function's docstring). The hook receives an ABSOLUTE file_path,
    # so anchor the body at a directory boundary with `(?:^|.*/)` — mirroring
    # the bare-filename branch above. `(?:^|/)` alone is unreachable under
    # re.Pattern.match() (it can only take the `^` branch at offset 0), which
    # made patterns like `src/third-party/**` silently fail on absolute paths.
    return re.compile(rf"(?:^|.*/){body}$")


# Cache compiled regex per pattern.
_REGEX_CACHE: dict[str, re.Pattern[str]] = {}


def _compile_cached(pattern: str) -> re.Pattern[str]:
    if pattern not in _REGEX_CACHE:
        _REGEX_CACHE[pattern] = _pattern_to_regex(pattern)
    return _REGEX_CACHE[pattern]


def matches_any_pattern(path_str: str, patterns: Iterable[str]) -> str | None:
    """Return the first pattern that matches `path_str`, or None.

    `path_str` is normalized to forward slashes regardless of platform so the
    same patterns work on Windows and Unix.
    """
    normalized = path_str.replace("\\", "/")
    for pattern in patterns:
        if _compile_cached(pattern).match(normalized):
            return pattern
    return None


def find_project_root(start: Path) -> Path | None:
    """Walk up from `start` to find a project root marker.

    A project root is the first ancestor directory containing any of:
    `.coding-standards-ignore`, `.git`, `package.json`, `pyproject.toml`,
    `Cargo.toml`, `go.mod`, `composer.json`, `pom.xml`, `build.gradle`,
    `requirements.txt`/`setup.py`/`setup.cfg` (plain Python), or a `*.csproj` /
    `*.sln` / `*.fsproj` file (.NET — matched by glob, not exact name).
    Returns None if no marker is found within 20 levels.
    """
    markers = {
        ".coding-standards-ignore", ".git", "package.json", "pyproject.toml",
        "Cargo.toml", "go.mod", "composer.json", "pom.xml", "build.gradle",
        "build.gradle.kts", "requirements.txt", "setup.py", "setup.cfg",
    }
    glob_markers = ("*.csproj", "*.sln", "*.fsproj")
    current = start if start.is_dir() else start.parent
    for _ in range(20):
        if any((current / m).exists() for m in markers):
            return current
        if any(next(current.glob(g), None) is not None for g in glob_markers):
            return current
        if current.parent == current:
            return None
        current = current.parent
    return None


def load_user_ignore_patterns(project_root: Path | None) -> tuple[str, ...]:
    """Load patterns from `.coding-standards-ignore` at the project root.

    Returns empty tuple if no such file exists or project_root is None.
    Lines starting with `#` and blank lines are ignored.
    """
    if project_root is None:
        return ()
    ignore_file = project_root / ".coding-standards-ignore"
    if not ignore_file.exists():
        return ()
    try:
        lines = ignore_file.read_text(encoding="utf-8").splitlines()
    except (OSError, UnicodeDecodeError):
        return ()
    patterns = []
    for line in lines:
        line = line.strip()
        if not line or line.startswith("#"):
            continue
        patterns.append(line)
    return tuple(patterns)


def is_excluded_path(file_path: str) -> tuple[bool, str | None]:
    """Check if a file path is excluded by any default or user pattern.

    Returns (excluded, matched_pattern_or_None).
    """
    # Defaults
    match = matches_any_pattern(file_path, DEFAULT_EXCLUSIONS)
    if match:
        return True, f"default:{match}"

    # User-defined
    project_root = find_project_root(Path(file_path))
    user_patterns = load_user_ignore_patterns(project_root)
    if user_patterns:
        match = matches_any_pattern(file_path, user_patterns)
        if match:
            return True, f"user:{match}"

    return False, None


def has_generation_marker(content: str, max_lines: int = 10) -> str | None:
    """Return the first generation marker found in the first `max_lines`
    lines of `content`, or None.
    """
    if not content:
        return None
    head = content.splitlines()[:max_lines]
    head_text = "\n".join(head).lower()
    for marker in GENERATION_MARKERS:
        if marker in head_text:
            return marker
    return None
